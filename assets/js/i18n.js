function applyTranslations(lang){
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;
  document.documentElement.setAttribute('data-lang', lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = (lang === 'ar') ? 'rtl' : 'ltr';
  // text nodes
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key = el.getAttribute('data-i18n');
    if(dict[key]) el.innerText = dict[key];
  });
  // placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el=>{
    const key = el.getAttribute('data-i18n-placeholder');
    if(dict[key]) el.setAttribute('placeholder', dict[key]);
  });
  // title
  if(dict['tagline.title']) document.title = `Auto Glow — ${dict['tagline.title']}`;
}

function initI18n(){
  const stored = localStorage.getItem('autoGlowLang') || (navigator.language||'en').slice(0,2);
  const lang = ['en','fr','ar'].includes(stored)?stored:'en';
  applyTranslations(lang);
  document.querySelectorAll('.lang-switcher [data-lang], .lang-switcher button[data-lang]').forEach(btn=>{
    btn.addEventListener('click',()=>{
      const l = btn.getAttribute('data-lang');
      localStorage.setItem('autoGlowLang', l);
      applyTranslations(l);
    });
  });
}

document.addEventListener('DOMContentLoaded',initI18n);

// expose translations for other scripts
window.TRANSLATIONS = TRANSLATIONS;
